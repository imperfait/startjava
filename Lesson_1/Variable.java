public class Variable {
	public static void main(String[] args) {
		byte cores = 4;
		short cpuFrequency = 1800;
		int ram = 16;
		long storageCapacity = 512L;
		float screen = 13.3f;
		double weight = 1.22d;
		char cpuType = 'U';
		boolean laptop = true;

		System.out.println("cores = " + cores);
		System.out.println("cpuFrequency = " + cpuFrequency);
		System.out.println("ram = " + ram);
		System.out.println("storageCapacity = " + storageCapacity);
		System.out.println("screen = " + screen);
		System.out.println("weight = " + weight);
		System.out.println("cpuType = " + cpuType);
		System.out.println("laptop = " + true);
	}
}